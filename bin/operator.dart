void main() {
  // +
  int plusNum1 = 3;
  int plusNum2 = 4;

  var plusResult = plusNum1 + plusNum2;
  print(plusResult);

  // -
  int minusNum1 = 3;
  int minusNum2 = 4;

  var minusResult = minusNum1 - minusNum2;
  print(minusResult);

  // *
  int multipleNum1 = 3;
  int multipleNum2 = 4;

  var multipleResult = multipleNum1 * multipleNum2;
  print(multipleResult);

  // /
  int divideNum1 = 3;
  int divideNum2 = 4;

  var divideResult = divideNum1 / divideNum2;
  print("Hasil pembagian $divideNum1 $divideNum2 = $divideResult");
}
